private void cargarTabla() {
    modelo.setRowCount(0); 
    List<Perfil> lista = DatosPerfil.listar(); 

    for (Perfil perfil : lista) {
        modelo.addRow(new Object[]{perfil.getCodigo(), perfil.getNombre()}); 
    }
    tblPerfil.setEnabled(true); 
}

package proyecto_gm.Horarios;

public class Horarios {
    private int idhorario;
    private String dni;
    private String celular;
    private String lunes_he, lunes_hs;
    private String martes_he, martes_hs;
    private String miercoles_he, miercoles_hs;
    private String jueves_he, jueves_hs;
    private String viernes_he, viernes_hs;
    private String sabado_he, sabado_hs;
    private String domingo_he, domingo_hs;

    public Horarios() {}

    public int getIdhorario() { return idhorario; }
    public void setIdhorario(int idhorario) { this.idhorario = idhorario; }

    public String getDni() { return dni; }
    public void setDni(String dni) { this.dni = dni; }

    public String getCelular() { return celular; }
    public void setCelular(String celular) { this.celular = celular; }

    public String getLunes_he() { return lunes_he; }
    public void setLunes_he(String lunes_he) { this.lunes_he = lunes_he; }

    public String getLunes_hs() { return lunes_hs; }
    public void setLunes_hs(String lunes_hs) { this.lunes_hs = lunes_hs; }

    public String getMartes_he() { return martes_he; }
    public void setMartes_he(String martes_he) { this.martes_he = martes_he; }

    public String getMartes_hs() { return martes_hs; }
    public void setMartes_hs(String martes_hs) { this.martes_hs = martes_hs; }

    public String getMiercoles_he() { return miercoles_he; }
    public void setMiercoles_he(String miercoles_he) { this.miercoles_he = miercoles_he; }

    public String getMiercoles_hs() { return miercoles_hs; }
    public void setMiercoles_hs(String miercoles_hs) { this.miercoles_hs = miercoles_hs; }

    public String getJueves_he() { return jueves_he; }
    public void setJueves_he(String jueves_he) { this.jueves_he = jueves_he; }

    public String getJueves_hs() { return jueves_hs; }
    public void setJueves_hs(String jueves_hs) { this.jueves_hs = jueves_hs; }

    public String getViernes_he() { return viernes_he; }
    public void setViernes_he(String viernes_he) { this.viernes_he = viernes_he; }

    public String getViernes_hs() { return viernes_hs; }
    public void setViernes_hs(String viernes_hs) { this.viernes_hs = viernes_hs; }

    public String getSabado_he() { return sabado_he; }
    public void setSabado_he(String sabado_he) { this.sabado_he = sabado_he; }

    public String getSabado_hs() { return sabado_hs; }
    public void setSabado_hs(String sabado_hs) { this.sabado_hs = sabado_hs; }

    public String getDomingo_he() { return domingo_he; }
    public void setDomingo_he(String domingo_he) { this.domingo_he = domingo_he; }

    public String getDomingo_hs() { return domingo_hs; }
    public void setDomingo_hs(String domingo_hs) { this.domingo_hs = domingo_hs; }
}

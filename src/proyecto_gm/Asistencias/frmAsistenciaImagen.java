    private static BufferedImage convertirBytesAImagen(byte[] bytes) {
        try {
            ByteArrayInputStream bis = new ByteArrayInputStream(bytes);
            return ImageIO.read(bis);
        } catch (IOException e) {
           
            return null;
        }
    }

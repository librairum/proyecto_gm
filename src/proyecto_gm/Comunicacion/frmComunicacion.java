    private void HabilitarBotones(boolean estado) {
        btnAgregar.setEnabled(!estado);
        btnEditar.setEnabled(!estado);
        btnEliminar.setEnabled(!estado);
        btnGuardar.setEnabled(estado);
        btnDeshacer.setEnabled(estado);
    }
